    {
        var cancellationToken = new CancellationTokenSource().Token;

        var token = GeradorTokenUsuarioBuilder.Build().Gerar(_usuarioAdministrador.Id);

        var sistemaCliente = await CadastroHelper.CadastrarNovoSistemaCliente(_usuarioAdministrador.Id);

        var request = RequestAlterarSistemaClienteBuilder.Build();
        request.Id = sistemaCliente.Id.ToString();

        var response = await HttpHelper.DoPut(url: _baseUrlSistemaCliente, request: request, token: token, cancellationToken: cancellationToken);

        Assert.That(response.StatusCode, Is.EqualTo(HttpStatusCode.NoContent));
    }

