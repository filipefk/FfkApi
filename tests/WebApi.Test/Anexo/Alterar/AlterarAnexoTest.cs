    public async Task Sucesso_Administrador()
    {
        var cancellationToken = new CancellationTokenSource().Token;

        var token = GeradorTokenUsuarioBuilder.Build().Gerar(_usuarioAdministrador.Id);

        var anexo = await CadastrarNovoAnexo();

        var request = RequestAlterarAnexoBuilder.Build();
        request.Id = anexo.Id.ToString();

        var response = await HttpHelper.DoPut(url: _baseUrlAnexo, request: request, token: token, cancellationToken: cancellationToken);

        Assert.That(response.StatusCode, Is.EqualTo(HttpStatusCode.NoContent));
    }

