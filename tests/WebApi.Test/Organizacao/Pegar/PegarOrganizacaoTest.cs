    public PegarOrganizacaoTest()
    {
        var entidades = _factory.EntidadesCriadas();
        _usuarioAdministrador = (FfkApi.Domain.Entities.Usuario)entidades["UsuarioAdministrador"];
        _usuarioPermissaoCadastroOrganizacoes = (FfkApi.Domain.Entities.Usuario)entidades["UsuarioPermissaoCadastroOrganizacoes"];
